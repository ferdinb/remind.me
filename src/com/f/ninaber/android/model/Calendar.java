package com.f.ninaber.android.model;

public class Calendar {
	private String day;
	private String dateMonthYear;
	private String numTask;

	public String getDay() {
		return day;
	}

	public void setDay(String day) {
		this.day = day;
	}

	public String getDateMonthYear() {
		return dateMonthYear;
	}

	public void setDateMonthYear(String dateMonthYear) {
		this.dateMonthYear = dateMonthYear;
	}

	public String getNumTask() {
		return numTask;
	}

	public void setNumTask(String numTask) {
		this.numTask = numTask;
	}

}
